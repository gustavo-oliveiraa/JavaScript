// Operadores lógicos (logical operators)

// - 2 valores booleanos, quando verificados,
// resulturá em verdadeiro ou falso

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo)

// OR ||
console.log(pao || queijo)

// NOT !
console.log(!pao) // Troca de valor, sempre que colocar uma ! na frente de uma variavel estou trocando o valor dela
